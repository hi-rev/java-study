package chapter04;

public class StringTest01 {

	public static void main(String[] args) {
		// c:\temp
		System.out.println("c:\\temp");
		
		// "HELLO"
		System.out.println("\"Hello\"");
		
		// \t: tab
		// \r: carriage return
		// \n: new line
		// \b: bell
		System.out.print("hello\tworld\n");
		System.out.println("hello\tworld");
		
		// '
		char c = '\'';
		
		System.out.print("1");
		System.out.print("2");
		System.out.print("3");
		
	}

}
